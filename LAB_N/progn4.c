#include <stdio.h>
#include <math.h> // Для використання функції cos()

int main() {
    float a = 0, b = 3, dx = 0.05; // Початок, кінець та крок
    float x, y;

    // Виведення заголовку таблиці
    printf("X\t\tY = 4^(-cos(X))\n");
    printf("----------------------------\n");

    // Цикл для обчислення значень функції на проміжку [0, 3] з кроком 0.05
    for (x = a; x <= b; x += dx) {
        y = pow(4, -cos(x)); // Обчислення 4^(-cos(x))
        printf("%.2f\t%.5f\n", x, y); // Виведення результату
    }

    return 0;
}
